# Print() will automatically add the new string at the end

print('hello')
print('world')

# You can add what you want to add at the end of the line
# for example let's use the space in this example

print('Hello', end=' ')
print('World')

# You can also define what separator to use with print() function

print('dog', 'cat', 'cow')
print('dog', 'cat', 'cow', sep=', ')
